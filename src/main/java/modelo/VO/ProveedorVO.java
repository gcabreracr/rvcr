
package modelo.VO;


public class ProveedorVO {
    
    private String nomProv;
    private String idProv;
    private String tipoIdProv;
    private String nomComProv;
    private String emailProv;
    private String teleProv;
    

    public ProveedorVO() {
    }

    public String getNomProv() {
        return nomProv;
    }

    public void setNomProv(String nomProv) {
        this.nomProv = nomProv;
    }

    public String getIdProv() {
        return idProv;
    }

    public void setIdProv(String idProv) {
        this.idProv = idProv;
    }

    public String getTipoIdProv() {
        return tipoIdProv;
    }

    public void setTipoIdProv(String tipoIdProv) {
        this.tipoIdProv = tipoIdProv;
    }

    public String getNomComProv() {
        return nomComProv;
    }

    public void setNomComProv(String nomComProv) {
        this.nomComProv = nomComProv;
    }

    public String getEmailProv() {
        return emailProv;
    }

    public void setEmailProv(String emailProv) {
        this.emailProv = emailProv;
    }

    public String getTeleProv() {
        return teleProv;
    }

    public void setTeleProv(String teleProv) {
        this.teleProv = teleProv;
    }
    
    
    
    
    
}
